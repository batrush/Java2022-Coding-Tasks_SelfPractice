package com.cydeo.conditionals;

public class Conditionals {

	public static void main(String[] args) {


		int num = 24;


		if(num < 0) {
			System.out.println(num + " is less than 0");
		}else if(num == 0){
			System.out.println(num + " equals to zero");
		}else {
			System.out.println(num + " is greater than zero");
		}

	}

}